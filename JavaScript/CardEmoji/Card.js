function Emoji() {
  let randomEmojiSeries = Math.floor(
    Math.random() * (128512 - 128567 + 1) + 128567
  );
  let newRandomEmojiSeries = "&#" + randomEmojiSeries.toString();
  document.getElementById("Emojicontainer").innerHTML = newRandomEmojiSeries;
}




















































































































































































